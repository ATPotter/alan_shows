<p>
A large number of the people who did the Forth Dimension shows (rehearsing
over the summer) were also members of the Bo'ness Amateur Operatic Society (rehearsing
over the winter), and so I joined that.
</p>

<p>
It turned out to be good fun, and I got to do <i>Guys And Dolls</i> again.
</p>

<p>
At one point in this, Sammy Clark - one of the dancers - had to slap my face.  One rehearsal,
she missed and instead hit my glasses, which went sailing across to the other side of the
rehearsal hall.  Luckily, they remained intact!
</p>


